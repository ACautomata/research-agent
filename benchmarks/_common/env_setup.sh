#!/usr/bin/env bash
# benchmarks/_common/env_setup.sh
#
# Unified pre-benchmark env. Boots a minimal openclaw-docker-cn-im container,
# mounts the current repo at /home/node/.openclaw, waits for `openclaw health`,
# and runs one smoke turn. Every benchmark's env.sh runs after this.
#
# Required env (set by CI workflow or by the user for local runs):
#   MINIMAX_API_KEY         -- LLM provider key (fail-fast if missing)
#   MINIMAX_BASE_URL        -- optional, defaults to https://api.minimaxi.com
#   BENCH_RUN_ID            -- used as the session key prefix and as the compose project name
#   BENCH_COMPOSE_FILE      -- optional, defaults to docker/docker-compose.bench.yml
#   BENCH_OPENCLAW_IMAGE    -- optional, overrides the image tag
#
# Exported after success:
#   BENCH_CONTAINER=openclaw-bench
#   BENCH_MOUNT=/home/node/.openclaw
#   BENCH_OPENCLAW=openclaw   (the path to the CLI inside the container)
set -euo pipefail

ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
COMPOSE_FILE="${BENCH_COMPOSE_FILE:-${ROOT}/docker/docker-compose.bench.yml}"
IMAGE="${BENCH_OPENCLAW_IMAGE:-justlikemaki/openclaw-docker-cn-im:latest}"
RUN_ID="${BENCH_RUN_ID:-local-$$}"
COMPOSE_PROJECT="openclaw-bench-${RUN_ID}"

log() { printf '\n[env_setup] %s\n' "$*"; }
die() { printf '\n[env_setup][FATAL] %s\n' "$*" >&2; exit 1; }

LOCAL_ENV_FILE="${ROOT}/docker/.env.bench"
if [[ -f "${LOCAL_ENV_FILE}" ]]; then
  log "loading local benchmark env ${LOCAL_ENV_FILE}"
  set -a
  # shellcheck disable=SC1090
  . "${LOCAL_ENV_FILE}"
  set +a
fi

# 0. Secrets check
if [[ -z "${MINIMAX_API_KEY:-}" ]]; then
  die "MINIMAX_API_KEY is not set. Add it as a GitHub Actions secret, then re-run."
fi
: "${MINIMAX_BASE_URL:=https://api.minimaxi.com}"

# 1. Build a temporary .env the compose file can read.
ENV_DIR="${ROOT}/.bench-runtime"
mkdir -p "${ENV_DIR}"
ENV_FILE="${ENV_DIR}/.env.bench"
log "writing ${ENV_FILE}"
cat >"${ENV_FILE}" <<EOF
# Auto-generated by benchmarks/_common/env_setup.sh (run id=${RUN_ID})
OPENCLAW_IMAGE=${IMAGE}
OPENCLAW_DATA_DIR=${ENV_DIR}/openclaw-data
OPENCLAW_RUN_USER=0:0
DOCKER_BIND=127.0.0.1
OPENCLAW_GATEWAY_PORT=18789
OPENCLAW_GATEWAY_BIND=127.0.0.1
TZ=Asia/Shanghai
SYNC_OPENCLAW_CONFIG=false
SYNC_EXTENSIONS_ON_START=false
SYNC_MODEL_CONFIG=true
MODEL_ID=minimax/MiniMax-M2.7
PRIMARY_MODEL=minimax/MiniMax-M2.7
BASE_URL=${MINIMAX_BASE_URL}
API_KEY=${MINIMAX_API_KEY}
MINIMAX_API_KEY=${MINIMAX_API_KEY}
API_PROTOCOL=anthropic
CONTEXT_WINDOW=200000
MAX_TOKENS=8192
DM_POLICY=disabled
GROUP_POLICY=disabled
ALLOW_FROM=
OPENCLAW_WORKSPACE_ROOT=/home/node/.openclaw
OPENCLAW_SANDBOX_MODE=off
OPENCLAW_PLUGINS_ENABLED=false
EOF
mkdir -p "${ENV_DIR}/openclaw-data"

# 2. Pull the image (idempotent). Use docker hub mirror if available.
log "pulling ${IMAGE}"
docker pull "${IMAGE}" >/dev/null

copy_repo_to_data_dir() {
  local dest="$1"
  log "pre-populating ${dest} with benchmark repo config/workspaces"
  mkdir -p "${dest}"
  find "${dest}" -mindepth 1 -delete 2>/dev/null || true
  tar --exclude='.git' --exclude='.github' --exclude='.env' \
      --exclude='docker/.env.bench' --exclude='*.sqlite*' \
      --exclude='qmd' --exclude='logs' --exclude='tasks' \
      --exclude='credentials' --exclude='cron' --exclude='devices' \
      --exclude='identity' --exclude='feishu' --exclude='extensions' \
      --exclude='qqbot' --exclude='.openclaw' --exclude='.dreams' \
      --exclude='dreaming' --exclude='.bench-runtime' \
      --exclude='bench-results' \
      -C "${ROOT}" -cf - . | tar -xf - -C "${dest}"
}

# 3. Bring up the gateway service. We pass the project name to isolate the
#    stack from any other compose project on the same host.
#
#    We `set -a; source` the env file so that docker compose's variable
#    substitution (e.g. `${OPENCLAW_IMAGE}`, `${OPENCLAW_DATA_DIR}`) resolves
#    from the keys we wrote. `--env-file` alone would only feed substitution
#    for variables referenced in the compose file, not the compose process's
#    own os.environ that some compose versions read for `${VAR}` lookups
#    inside service definitions.
log "bringing up compose (project=${COMPOSE_PROJECT})"
set -a
# shellcheck disable=SC1090
. "${ENV_FILE}"
set +a
# Allow the data dir to be overridden by the .env file (it is written there),
# but fall back to a host-local path that we create ourselves.
: "${OPENCLAW_DATA_DIR:=${ENV_DIR}/openclaw-data}"
mkdir -p "${OPENCLAW_DATA_DIR}"
copy_repo_to_data_dir "${OPENCLAW_DATA_DIR}"
docker compose --project-name "${COMPOSE_PROJECT}" \
    -f "${COMPOSE_FILE}" --env-file "${ENV_FILE}" \
    up -d openclaw-bench

# 4. Wait for `openclaw health` to return ready.
CONTAINER="$(docker ps --filter "label=com.docker.compose.project=${COMPOSE_PROJECT}" \
              --format '{{.Names}}' | head -n1)"
[[ -n "${CONTAINER}" ]] || die "no container found for project ${COMPOSE_PROJECT}"
log "container: ${CONTAINER}"

# 5. Copy the repo into the container's /home/node/.openclaw so the openclaw
#    gateway sees the checked-out tree. We use `docker cp` rather than a
#    host-side rsync into the docker volume because the runner user can't
#    write into the volume (root-owned), and `docker cp` is unprivileged.
log "docker cp repo into ${CONTAINER}:/home/node/.openclaw"
# Clear any pre-existing files inside the mount (except hidden .openclaw
# artifacts the image may have placed there).
docker exec "${CONTAINER}" bash -lc '
  set -e
  cd /home/node/.openclaw
  # remove everything except the directory itself so hidden runtime state
  # (extensions/, qmd/, etc.) created by the image stays intact.
  find . -mindepth 1 -delete 2>/dev/null || true
'
# Stream a tarball of the repo (with the same excludes) and untar inside the
# container. This avoids needing host-side write access to the volume.
tar --exclude='.git' --exclude='.github' --exclude='.env' \
    --exclude='docker/.env.bench' \
    --exclude='*.sqlite*' --exclude='qmd' --exclude='logs' \
    --exclude='tasks' --exclude='credentials' --exclude='cron' \
    --exclude='devices' --exclude='identity' --exclude='feishu' \
    --exclude='extensions' --exclude='qqbot' --exclude='.openclaw' \
    --exclude='.dreams' --exclude='dreaming' --exclude='.bench-runtime' \
    --exclude='bench-results' \
    -C "${ROOT}" -cf - . | \
  docker exec -i "${CONTAINER}" tar -xf - -C /home/node/.openclaw
# init.sh runs the openclaw gateway as the in-image node user (uid 1000).
# The files we just streamed in are owned by root (since `docker exec tar`
# runs as the container's user -- which is root here -- and tar preserves
# host uids). The gateway then fails to write logs/, qmd/, .config/, etc.
# chown the whole tree to uid 1000:1000 to match the runtime user.
log "chown /home/node/.openclaw -> 1000:1000 (openclaw runtime uid)"
docker exec "${CONTAINER}" chown -R 1000:1000 /home/node/.openclaw || true
log "repo copied into container"

# Belt-and-suspenders: even if init.sh's config sync already wrote
# auth-profiles.json, we re-run `openclaw models auth paste-token` so the
# gateway's auth store definitely has the minimax credential for the
# embedded (--local) agent calls below. This is the path that fails
# noisily with 'No API key found for provider "minimax"' if the secret
# is missing.
if [[ -n "${MINIMAX_API_KEY:-}" ]]; then
  log "setting models.providers.minimax.apiKey to MINIMAX_API_KEY via openclaw config set"
  # The gateway's credential lookup is driven by `models.providers.<id>.apiKey`.
  # Per docs.openclaw.ai/gateway/secrets + openclawsome.com guide, the
  # value should be a SecretRef object: {source:"env", provider:"default",
  # id:"<ENV_VAR_NAME>"}. openclaw then resolves it at runtime from the
  # forwarded MINIMAX_API_KEY env var. This is the supported, secret-safe
  # path -- no real key is ever written to disk.
  patch_secret_ref() {
    docker exec "${CONTAINER}" bash -lc '
      export HOME=/home/node
      REF='\''{"source":"env","provider":"default","id":"MINIMAX_API_KEY"}'\''
      openclaw config set --json models.providers.minimax.apiKey "$REF" \
        2>/dev/null || true
      python3 - <<'\''PY'\''
import json, pathlib
p = pathlib.Path("/home/node/.openclaw/openclaw.json")
data = json.loads(p.read_text(encoding="utf-8"))
prov = data.setdefault("models", {}).setdefault("providers", {}).setdefault("minimax", {})
prov["apiKey"] = {"source": "env", "provider": "default", "id": "MINIMAX_API_KEY"}
p.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
PY
      chown -R 1000:1000 /home/node/.openclaw/openclaw.json
    '
  }
  # First patch (before restart), then restart, then re-patch (init.sh
  # regenerates openclaw.json on every container start).
  patch_secret_ref
  log "restarting openclaw-bench so it re-resolves the SecretRef snapshot"
  docker compose --project-name "${COMPOSE_PROJECT}" \
    -f "${COMPOSE_FILE}" --env-file "${ENV_FILE}" \
    restart openclaw-bench
  # After restart, init.sh re-ran. Re-derive the container name (the id
  # is new, but container_name=openclaw-bench is stable) and re-wait for
  # the gateway ready marker before re-patching.
  sleep 3
  RESTART_READY=0
  for i in $(seq 1 60); do
    STATE="$(docker inspect --format '{{.State.Running}}' openclaw-bench 2>/dev/null || true)"
    if [[ "${STATE}" != "true" ]]; then
      die "container openclaw-bench exited after restart (state=${STATE})"
    fi
    # Use --since to only match logs from AFTER the restart, avoiding false
    # positives from the first run's "http server listening" line.
    if docker logs --since 10s --tail 20 openclaw-bench 2>&1 | grep -qE 'http server listening|heartbeat.*started'; then
      RESTART_READY=1
      log "gateway ready after restart (poll $i)"
      break
    fi
    sleep 2
  done
  if [[ "${RESTART_READY}" -ne 1 ]]; then
    echo "[env_setup][FATAL] gateway not ready after restart; dumping logs:" >&2
    docker logs --tail 200 openclaw-bench >&2 || true
    exit 1
  fi
  patch_secret_ref
  log "post-restart SecretRef:"
  docker exec openclaw-bench python3 -c \
    'import json; d=json.load(open("/home/node/.openclaw/openclaw.json")); print(d["models"]["providers"]["minimax"].get("apiKey"))' || true
  # Re-export the (re-derived) container name for downstream steps.
  CONTAINER="openclaw-bench"
else
  log "MINIMAX_API_KEY not set; skipping apiKey patch"
fi

# 6. Wait for the openclaw container to start and the gateway to become
#    ready. The image's healthcheck is informational only (we set
#    restart=no), so we don't rely on `health=healthy`. We poll the
#    container's State.Running and tail its log for a known ready marker.
log "waiting for ${CONTAINER} to come up and gateway to be ready"
READY=0
for i in $(seq 1 90); do
  STATE="$(docker inspect --format '{{.State.Running}}' "${CONTAINER}" 2>/dev/null || true)"
  if [[ "${STATE}" == "true" ]]; then
    # Look for the gateway-ready marker in the container log.
    if docker logs --tail 200 "${CONTAINER}" 2>&1 | grep -qE 'http server listening|starting channels and sidecars|\[gateway\] ready|heartbeat.*started'; then
      READY=1
      log "container ${CONTAINER} is up and gateway is ready after ${i} polls"
      break
    fi
  fi
  # Detect a hard exit (init.sh crashed). Bail loudly so the workflow fails.
  RUNNING="$(docker inspect --format '{{.State.Running}}|{{.State.Status}}' "${CONTAINER}" 2>/dev/null || true)"
  case "${RUNNING}" in
    false|exited|dead|removing)
      echo "[env_setup][FATAL] container ${CONTAINER} state=${RUNNING}; dumping logs:" >&2
      docker logs --tail 200 "${CONTAINER}" >&2 || true
      exit 1
      ;;
  esac
  sleep 2
done
if [[ "${READY}" -ne 1 ]]; then
  echo "[env_setup][FATAL] gateway not ready in 180s" >&2
  docker logs --tail 200 "${CONTAINER}" >&2 || true
  exit 1
fi

log "running main-agent smoke check"
if ! docker exec -e MINIMAX_API_KEY -e MINIMAX_BASE_URL "${CONTAINER}" \
  openclaw agent --agent main --message "ping" --local --json \
  --session-key "agent:main:bench-${RUN_ID}-smoke" --timeout 120 >/dev/null; then
  echo "[env_setup][FATAL] main-agent smoke check failed; dumping logs:" >&2
  docker logs --tail 200 "${CONTAINER}" >&2 || true
  exit 1
fi

# 8. Export the contract for downstream env.sh scripts AND for subsequent
#    GitHub Actions steps. We can't `export` from a subshell back into the
#    caller, so we both export (for any subshells in this step) and write
#    a small env file the next workflow step can `source`.
export BENCH_CONTAINER="${CONTAINER}"
export BENCH_MOUNT="/home/node/.openclaw"
export BENCH_OPENCLAW="openclaw"
export BENCH_COMPOSE_PROJECT="${COMPOSE_PROJECT}"
export BENCH_ENV_FILE="${ENV_FILE}"
export BENCH_DATA_DIR="${ENV_DIR}/openclaw-data"

# Drop a sourceable env file for subsequent workflow steps.
EXPORT_FILE="${ENV_DIR}/bench-runtime-env.sh"
cat >"${EXPORT_FILE}" <<EOF
# Auto-sourced by .github/workflows/benchmark.yml after env_setup.sh.
export BENCH_CONTAINER='${CONTAINER}'
export BENCH_MOUNT='/home/node/.openclaw'
export BENCH_OPENCLAW='openclaw'
export BENCH_COMPOSE_PROJECT='${COMPOSE_PROJECT}'
export BENCH_ENV_FILE='${ENV_FILE}'
export BENCH_DATA_DIR='${ENV_DIR}/openclaw-data'
EOF
# Surface the file path so the workflow can source it without re-deriving.
if [[ -n "${GITHUB_ENV:-}" ]]; then
  echo "BENCH_ENV_FILE=${EXPORT_FILE}" >> "${GITHUB_ENV}"
  echo "BENCH_CONTAINER=${CONTAINER}" >> "${GITHUB_ENV}"
  echo "BENCH_MOUNT=/home/node/.openclaw" >> "${GITHUB_ENV}"
  echo "BENCH_COMPOSE_PROJECT=${COMPOSE_PROJECT}" >> "${GITHUB_ENV}"
fi

log "ready: container=${BENCH_CONTAINER} mount=${BENCH_MOUNT} data_dir=${BENCH_DATA_DIR}"
log "runtime env file: ${EXPORT_FILE}"
